package com.github.giandemanincor.introduction;

public class Class05_RepetitionStructure03 {
    public static void main(String[] args) {
        // print the first 25 numbers of a value

        for (int i = 0; i < 50; i++) {
            if (i > 25) {
                break;
            }
            System.out.println(i);
        }
    }
}
